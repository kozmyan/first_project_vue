<template>
  <div>
    <form @submit.prevent>
      <my-input 
      v-model="post.title" 
      type="text" 
      placeholder="Название"/>

      <my-input 
      v-model="post.body" 
      type="text" 
      placeholder="Описание"/>

      <my-button 
      @click = "createPost" 
      class="btn_big"
      >Создать
      </my-button>
    </form>

  </div>
</template>



<script>

export default {
   
  data() {
    return {
       post: {
        title: '',
        body: '',
       }
    }
  },
  methods: {
      createPost () {
              this.post.id = Date.now();
              this.$emit('create', this.post);
              this.post = {
                title: '',
                body: '', 
               }
      },
  }
}
</script>




<style scoped>


 form{
   display: flex;
   flex-direction: column;
 }

 .btn_big{
  margin-top: 20px;
 }

 
</style>